function incrementVeg() {
    let counterElement = document.getElementById("counter");
    let currentCount = parseInt(counterElement.innerHTML);
    counterElement.innerHTML = currentCount + 1;
  }
  
  function decrementVeg() {
    let counterElement = document.getElementById("counter");
    let currentCount = parseInt(counterElement.innerHTML);
    counterElement.innerHTML = currentCount - 1;
  }



